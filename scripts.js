document.getElementById('inputText').addEventListener('input', function() {
    const inputText = this.value;
    generateOutput(inputText);
});

function generateOutput(text) {
    const outputContainer = document.getElementById('outputContainer');
    outputContainer.innerHTML = '';

    const fancyFonts = {
        fancy1: { text: convertToFancy1(text), caption: 'Cursive' },
        fancy2: { text: convertToFancy2(text), caption: 'Bold Italic' },
        fancy3: { text: convertToFancy3(text), caption: 'Gothic' },
        fancy4: { text: convertToFancy4(text), caption: 'Bold' },
        fancy5: { text: convertToFancy5(text), caption: 'Script' },
        fancy6: { text: convertToFancy6(text), caption: 'Wide' },
        fancy7: { text: convertToFancy7(text), caption: 'Small Caps' },
        fancy8: { text: convertToFancy8(text), caption: 'Serif' },
        fancy9: { text: convertToFancy9(text), caption: 'Monospace' },
        reverse: { text: text.split('').reverse().join(''), caption: 'Reversed' },
        upperCase: { text: text.toUpperCase(), caption: 'Upper Case' },
        lowerCase: { text: text.toLowerCase(), caption: 'Lower Case' },
    };

    for (const [styleName, { text: styledText, caption }] of Object.entries(fancyFonts)) {
        const div = document.createElement('div');
        div.classList.add('outputItem');
        const span = document.createElement('span');
        span.innerHTML = `<strong>${caption}:       </strong> ${styledText}`;
        const button = document.createElement('button');
        button.classList.add('copyBtn');
        button.textContent = 'Copy';
        button.addEventListener('click', function() {
            copyToClipboard(styledText);
        });
        div.appendChild(span);
        div.appendChild(button);
        outputContainer.appendChild(div);
    }
}
